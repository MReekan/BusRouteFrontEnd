package com.example.busroute;

public class TravellingPath {
    public String id;
    public String ticket_fee;

    public TravellingPath() {

    }


    public TravellingPath(String ticket_fee) {
       this.id = id;
       this.ticket_fee= ticket_fee;
    }

    public String getId() {
        return id;
    }


    public String getTicket_fee() {
        return ticket_fee;
    }




}
